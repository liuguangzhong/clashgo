// validateClashCore 验证并修正 clash_core 字段
func (m *Manager) validateClashCore(v *IVerge) error {
	if v.ClashCore == nil {
		core := "verge-mihomo"
		v.ClashCore = &core
		return writeYAML(utils.Dirs().VergePath(), v, "# ClashGo Config")
	}
	for _, valid := range ValidClashCores {
		if *v.ClashCore == valid {
			return nil
		}
	}
	utils.Log().Warn("Invalid clash_core, resetting to default",
		zap.String("invalid", *v.ClashCore))
	core := "verge-mihomo"
	v.ClashCore = &core
	return writeYAML(utils.Dirs().VergePath(), v, "# ClashGo Config")
}

// readYAML 从文件读取并反序列化 YAML
func readYAML[T any](path string) (*T, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("read %s: %w", path, err)
	}
	var out T
	if err := yaml.Unmarshal(data, &out); err != nil {
		return nil, fmt.Errorf("parse %s: %w", path, err)
	}
	return &out, nil
}

// readYAMLMap 从文件读取 YAML 为 map
func readYAMLMap(path string) (map[string]interface{}, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var out map[string]interface{}
	if err := yaml.Unmarshal(data, &out); err != nil {
		return nil, err
	}
	if out == nil {
		out = make(map[string]interface{})
	}
	return out, nil
}

// writeYAML 将结构体序列化为 YAML 并原子写入文件（带注释前缀）
// 委托给 WriteYAMLAtomic：强制字符串引号（防折行）+ tmp→rename 原子写
func writeYAML(path string, v interface{}, comment string) error {
	return WriteYAMLAtomic(path, v, comment)
}

