// GetVerge 返回当前 IVerge 的副本（只读）
func (m *Manager) GetVerge() IVerge {
	m.mu.RLock()
	defer m.mu.RUnlock()
	if m.verge == nil {
		return DefaultVerge()
	}
	return *m.verge
}

// PatchVerge 将 patch 中非 nil 字段合并到当前配置并持久化
func (m *Manager) PatchVerge(patch IVerge) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	applyVergePatch(m.verge, &patch)

	if err := writeYAML(utils.Dirs().VergePath(), m.verge, "# ClashGo Config"); err != nil {
		return fmt.Errorf("verge save: %w", err)
	}
	return nil
}

// ─────────────────────────────────────────────────────────────────────────────
// Clash 基础配置操作
// ─────────────────────────────────────────────────────────────────────────────

// GetClash 返回 Clash 基础配置副本
func (m *Manager) GetClash() IClashBase {
	m.mu.RLock()
	defer m.mu.RUnlock()
	clone := make(IClashBase, len(m.clash))
	for k, v := range m.clash {
		clone[k] = v
	}
	return clone
}

// PatchClash 合并补丁并保存
func (m *Manager) PatchClash(patch map[string]interface{}) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	for k, v := range patch {
		m.clash[k] = v
	}

	if err := writeYAML(utils.Dirs().ClashPath(), m.clash, "# Generated by ClashGo"); err != nil {
		return fmt.Errorf("clash save: %w", err)
	}
	return nil
}

// DeleteClashKey 从 clash 基础配置中完全删除指定键（避免写入 null 覆盖 profile 配置）
func (m *Manager) DeleteClashKey(key string) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	delete(m.clash, key)

	if err := writeYAML(utils.Dirs().ClashPath(), m.clash, "# Generated by ClashGo"); err != nil {
		return fmt.Errorf("clash save (delete key): %w", err)
	}
	return nil
}

