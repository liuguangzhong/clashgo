// RuntimeSnapshot 运行时配置的只读快照（不含 mutex，可安全按值传递）
type RuntimeSnapshot struct {
	Config     IClashBase             `json:"config"`
	ExistsKeys map[string]bool        `json:"exists_keys"`
	ChainLogs  map[string][][2]string `json:"chain_logs"`
}

// GetRuntime 返回运行时配置的只读快照
// m.mu 的 RLock 已持有时可以安全读取 m.runtime
func (m *Manager) GetRuntime() RuntimeSnapshot {
	m.mu.RLock()
	defer m.mu.RUnlock()
	if m.runtime == nil {
		return RuntimeSnapshot{
			Config:     IClashBase{},
			ExistsKeys: map[string]bool{},
			ChainLogs:  map[string][][2]string{},
		}
	}
	// Manager.mu 已持有 RLock，可直接读取 RuntimeConfig 字段
	return RuntimeSnapshot{
		Config:     m.runtime.Config,
		ExistsKeys: m.runtime.ExistsKeys,
		ChainLogs:  m.runtime.ChainLogs,
	}
}

// SetRuntime 更新运行时配置（增强流水线完成后调用）
func (m *Manager) SetRuntime(r *RuntimeConfig) {
	m.mu.Lock()
	defer m.mu.Unlock()
	m.runtime = r
}

