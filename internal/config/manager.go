// Manager 全局配置管理器
// 负责 verge.yaml / clash.yaml / profiles.yaml / runtime 的读写与并发安全
type Manager struct {
	mu sync.RWMutex

	verge    *IVerge
	clash    IClashBase
	profiles *IProfiles
	runtime  *RuntimeConfig
}

var (
	globalManager *Manager
	managerOnce   sync.Once
)

// NewManager 初始化并返回配置管理器（同时从磁盘加载所有配置）
func NewManager() (*Manager, error) {
	m := &Manager{
		runtime: NewRuntimeConfig(),
	}

	if err := m.load(); err != nil {
		return nil, fmt.Errorf("config manager load: %w", err)
	}

	managerOnce.Do(func() {
		globalManager = m
	})

	return m, nil
}

// Global 返回全局配置管理器（必须在 NewManager 之后调用）
func Global() *Manager {
	if globalManager == nil {
		panic("config.Manager not initialized")
	}
	return globalManager
}

// load 从磁盘读取所有配置文件
func (m *Manager) load() error {
	dirs := utils.Dirs()
	log := utils.Log()

	// 1. 加载 verge.yaml
	verge, err := readYAML[IVerge](dirs.VergePath())
	if err != nil {
		log.Warn("Failed to load verge.yaml, using defaults", zap.Error(err))
		def := DefaultVerge()
		verge = &def
		// 写入默认值
		if werr := writeYAML(dirs.VergePath(), verge, "# ClashGo Config"); werr != nil {
			log.Warn("Failed to write default verge.yaml", zap.Error(werr))
		}
	}
	// 验证并修正 clash_core
	if err := m.validateClashCore(verge); err != nil {
		log.Warn("Failed to validate clash_core", zap.Error(err))
	}
	m.verge = verge

	// 2. 加载 clash.yaml（Mihomo 基础配置）
	clash, err := readYAMLMap(dirs.ClashPath())
	if err != nil {
		log.Warn("Failed to load clash.yaml, using defaults", zap.Error(err))
		clash = defaultClashBase()
		_ = writeYAML(dirs.ClashPath(), clash, "# Generated by ClashGo")
	} else {
		// 迁移：确保关键配置（DNS、TUN 进程排除）存在，兼容旧版 clash.yaml
		if migrateClashBase(clash) {
			log.Info("clash.yaml migrated: added missing dns/tun defaults")
			_ = writeYAML(dirs.ClashPath(), clash, "# Generated by ClashGo")
		}
	}
	m.clash = clash

	// 3. 加载 profiles.yaml
	profiles, err := readYAML[IProfiles](dirs.ProfilesPath())
	if err != nil {
		log.Warn("Failed to load profiles.yaml, using empty", zap.Error(err))
		profiles = &IProfiles{Items: []*IProfile{}}
		_ = writeYAML(dirs.ProfilesPath(), profiles, "# ClashGo Profiles")
	}
	m.profiles = profiles

	log.Info("Config loaded",
		zap.String("core", verge.GetClashCore()),
		zap.Int("profiles", len(profiles.Items)),
	)

	return nil
}

