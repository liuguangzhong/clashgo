// GetProfiles 返回所有 profiles
func (m *Manager) GetProfiles() IProfiles {
	m.mu.RLock()
	defer m.mu.RUnlock()
	if m.profiles == nil {
		return IProfiles{}
	}
	return *m.profiles
}

// GetCurrentProfile 返回当前激活的 Profile
func (m *Manager) GetCurrentProfile() *IProfile {
	m.mu.RLock()
	defer m.mu.RUnlock()
	if m.profiles == nil || m.profiles.Current == nil {
		return nil
	}
	for _, p := range m.profiles.Items {
		if p.UID != nil && *p.UID == *m.profiles.Current {
			return p
		}
	}
	return nil
}

// AddProfile 添加新 Profile 并保存
func (m *Manager) AddProfile(p *IProfile) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	now := time.Now()
	p.UpdatedAt = &now

	m.profiles.Items = append(m.profiles.Items, p)
	return m.saveProfiles()
}

// UpdateProfile 更新已有 Profile
func (m *Manager) UpdateProfile(uid string, patch *IProfile) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	for _, p := range m.profiles.Items {
		if p.UID != nil && *p.UID == uid {
			applyProfilePatch(p, patch)
			now := time.Now()
			p.UpdatedAt = &now
			return m.saveProfiles()
		}
	}
	return fmt.Errorf("profile not found: %s", uid)
}

// DeleteProfile 删除 Profile
func (m *Manager) DeleteProfile(uid string) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	items := m.profiles.Items[:0]
	found := false
	for _, p := range m.profiles.Items {
		if p.UID != nil && *p.UID == uid {
			found = true
			// 删除对应文件
			dirs := utils.Dirs()
			if p.File != nil {
				_ = os.Remove(dirs.ProfilesDir() + "/" + *p.File)
			}
			continue
		}
		items = append(items, p)
	}
	if !found {
		return fmt.Errorf("profile not found: %s", uid)
	}

	// 如果删除的是当前使用的 profile，清空 current
	if m.profiles.Current != nil && *m.profiles.Current == uid {
		m.profiles.Current = nil
	}
	m.profiles.Items = items
	return m.saveProfiles()
}

// SetCurrentProfile 设置当前激活的 Profile
func (m *Manager) SetCurrentProfile(uid string) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	found := false
	for _, p := range m.profiles.Items {
		if p.UID != nil && *p.UID == uid {
			found = true
			break
		}
	}
	if !found {
		return fmt.Errorf("profile not found: %s", uid)
	}
	m.profiles.Current = &uid
	return m.saveProfiles()
}

func (m *Manager) saveProfiles() error {
	return writeYAML(utils.Dirs().ProfilesPath(), m.profiles, "# ClashGo Profiles")
}

// ReorderProfiles 替换整个 profiles 列表（拖拽排序后用）
func (m *Manager) ReorderProfiles(items []*IProfile) error {
	m.mu.Lock()
	defer m.mu.Unlock()
	m.profiles.Items = items
	return m.saveProfiles()
}

