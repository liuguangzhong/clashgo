// migrateClashBase 检查并补全旧版 clash.yaml 中缺失的关键配置。
// 返回 true 表示有字段被修改，调用方应将改动写回磁盘。
//
// 修复场景：
//  1. 无 dns 配置 → TUN + 空 profile 时 dns-hijack 没有上游，所有域名 SERVFAIL
//  2. TUN 缺少 exclude-process-name → clashgo 自身 HTTP 请求被 TUN 捕获后走代理，
//     代理节点失效时订阅无法更新（Win/Linux 均适用）
func migrateClashBase(clash IClashBase) (changed bool) {
	// ── 1. 补全 DNS 配置 ──────────────────────────────────────────────────────
	if _, hasDNS := clash["dns"]; !hasDNS {
		clash["dns"] = map[string]interface{}{
			"enable": true,
			"ipv6":   false,
			"default-nameserver": []string{
				"223.5.5.5",
				"119.29.29.29",
			},
			"nameserver": []string{
				"https://dns.alidns.com/dns-query",
				"https://doh.pub/dns-query",
				"223.5.5.5",
			},
			"enhanced-mode": "fake-ip",
			"fake-ip-range": "198.18.0.1/16",
			"fake-ip-filter": []string{
				"*.lan",
				"+.stun.*.*",
				"+.stun.*.*.*",
				"localhost.ptlogin2.qq.com",
			},
		}
		changed = true
	}

	// ── 2. 补全 TUN exclude-process-name ─────────────────────────────────────
	if tunRaw, hasTUN := clash["tun"]; hasTUN {
		if tun, ok := tunRaw.(map[string]interface{}); ok {
			if _, hasExclude := tun["exclude-process-name"]; !hasExclude {
				tun["exclude-process-name"] = []string{"clashgo", "clashgo.exe"}
				clash["tun"] = tun
				changed = true
			}
		}
	}

	// ── 3. 修正 external-controller 端口（19097 → 9097 历史 bug 修复）──────────
	if ctrl, ok := clash["external-controller"].(string); ok && ctrl == "127.0.0.1:19097" {
		clash["external-controller"] = "127.0.0.1:9097"
		changed = true
	}
	return
}

